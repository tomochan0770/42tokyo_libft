#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;
	int		len;

	len = ft_strlen(s1);
	if (!(dest = malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

#include "libft.h"

size_t		ft_strlcpy(char *dst, char *src, size_t dstsize)
{
	size_t i;
	size_t src_len;

	if (!dst || !src)
		return (0);
	src_len = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
		return (src_len);
	while (i < dstsize - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_len);
}
